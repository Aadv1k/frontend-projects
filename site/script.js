const themeToggle = document.getElementById("toggle");
const icon = document.getElementById("themeIcon");
const target = document.getElementsByTagName("body")[0];

const theme = window.matchMedia("(prefers-color-scheme: dark)").matches
  ? "dark"
  : "light";

target.classList.toggle(theme);

const innerIcon = theme === "dark" ? "fa-sun" : "fa-moon";
icon.classList.add(innerIcon);

themeToggle.addEventListener("click", () => {
  const currentIcon = icon.classList.contains("fa-sun") ? "fa-sun" : "fa-moon";
  const altIcon = currentIcon === "fa-sun" ? "fa-moon" : "fa-sun";

  target.classList.toggle("dark");

  icon.classList.remove(currentIcon);
  icon.classList.add(altIcon);
});

const container = document.getElementsByClassName("card-container")[0];
let htmlChunk = "";
for (let i = 0; i < data.length; i += 1) {
  let linkChunk = "";
  let flairChunk = "";

  const { title } = data[i];
  const imgUrl = data[i].image;
  const { labels } = data[i];
  const { links } = data[i];

  for (const label of labels) {
    flairChunk += `
           <span class="flair">${label}</span>
        `;
  }

  for (const link in links) {
    if (links[link] !== "") {
      const linkName = link.toLowerCase().split(" ");
      linkName.push("card");

      linkChunk += `
        <a target="_blank" class="${linkName.join("-")}" href="${
        links[link]
      }">${link}</a>
        `;
    }
  }

  htmlChunk += `
    <div class="card">
      <div class="card__img">
        <img src="${imgUrl}" alt="" />
      </div>
      <div class="card__content">
        <h1 class="card__title">${title}</h1>
        <div class="card__labels">
            ${flairChunk}
        </div>
        <div class="card__links">
            ${linkChunk}
        </div>
      </div>
      </div>
    `;
}

container.innerHTML = htmlChunk;
